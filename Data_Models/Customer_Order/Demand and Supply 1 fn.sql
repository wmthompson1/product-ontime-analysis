        SELECT DISTINCT ISNULL(DSL.SUPPLY_TYPE,'CO') AS SUPPLY_TYPE, ISNULL(DSL.DEMAND_TYPE,'WO') AS DEMAND_TYPE,
		CL.CUST_ORDER_ID, CL.LINE_NO, CL.ORDER_QTY, CL.TOTAL_SHIPPED_QTY, CL.PART_ID, CL.UNIT_PRICE, 
		CL.LAST_SHIPPED_DATE, ISNULL(W2.BASE_ID,W.BASE_ID)+'/'+ISNULL(W2.LOT_ID,W.LOT_ID)+'/'+
		CAST(ISNULL(W2.SPLIT_ID,W.SPLIT_ID)AS VARCHAR) AS WORK_ORDER, ISNULL(W2.BASE_ID,W.BASE_ID) AS WO_BASE,
		ISNULL(W2.LOT_ID,W.LOT_ID) AS WO_LOT, ISNULL(W2.SPLIT_ID, W.SPLIT_ID) AS WO_SPLIT, CAST (0 AS INT) AS WO_SUB,
		CASE WHEN DSL.SUPPLY_TYPE IS NOT NULL THEN 'Y' ELSE 'N' END AS ALLOCATED, TRT.TRACE_ID, DSL.ALLOCATED_QTY, 
		DSL.RECEIVED_QTY, DSL.ISSUED_QTY, DSL.CREATE_DATE, DSL.[USER_ID]  
		FROM (CUST_ORDER_LINE CL 
        INNER JOIN CUSTOMER_ORDER CO ON CL.CUST_ORDER_ID = CO.ID)
		LEFT OUTER JOIN (
