                        DEMAND_SUPPLY_LINK DSL 
                        INNER JOIN WORK_ORDER W ON DSL.SUPPLY_BASE_ID = W.BASE_ID 
                        AND DSL.SUPPLY_LOT_ID = W.LOT_ID AND DSL.SUPPLY_SPLIT_ID = W.SPLIT_ID AND DSL.SUPPLY_SUB_ID = W.SUB_ID
                        AND DSL.SUPPLY_TYPE IN ('WO','CP')
                        ) 
        
        ON CL.CUST_ORDER_ID = DSL.DEMAND_BASE_ID AND CL.LINE_NO = DSL.DEMAND_SEQ_NO 
		AND DSL.DEMAND_TYPE = 'CO'
		LEFT OUTER JOIN (SHIPPER_LINE SL INNER JOIN TRACE_INV_TRANS TRT ON SL.TRANSACTION_ID = TRT.TRANSACTION_ID
		INNER JOIN WORK_ORDER W2 ON TRT.TRACE_ID = W2.BASE_ID+'/'+W2.LOT_ID+'/'+CAST(W2.SPLIT_ID AS VARCHAR) 
		AND W2.SUB_ID = 0) ON CL.CUST_ORDER_ID = SL.CUST_ORDER_ID AND CL.LINE_NO = SL.CUST_ORDER_LINE_NO