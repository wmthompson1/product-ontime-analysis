, CTE_PIVOT AS
	(	SELECT BASE_ID, LOT_ID, SPLIT_ID, SUB_ID
			, [M] AS MFG_COUNT, [A] AS FIN_COUNT
			, [O] AS O_COUNT, [NULL] AS NULL_COUNT
		FROM (	SELECT WO.BASE_ID, WO.LOT_ID, WO.SPLIT_ID, WO.SUB_ID, 
					CASE
						WHEN ISNULL(SR.USER_1, '') = '' THEN 'NULL'
						WHEN ISNULL(SR.USER_1, '') = 'B' OR ISNULL(SR.SKILLS_TYPE, '') = 'A' THEN 'A'
						WHEN ISNULL(SR.USER_1, '') = 'ARCHIVED' THEN ISNULL(SR.SKILLS_TYPE, '')
						ELSE ISNULL(SR.USER_1, '')
					END AS SKILLS_TYPE
				FROM CTE_BASE WO WITH (NOLOCK)
				LEFT OUTER JOIN OPERATION O WITH (NOLOCK) ON WO.BASE_ID = O.WORKORDER_BASE_ID AND 
					WO.LOT_ID = O.WORKORDER_LOT_ID AND WO.SPLIT_ID = O.WORKORDER_SPLIT_ID AND WO.SUB_ID = O.WORKORDER_SUB_ID
				LEFT OUTER JOIN SHOP_RESOURCE SR WITH (NOLOCK) ON O.RESOURCE_ID = SR.ID
			) AS SOURCE PIVOT (COUNT(SKILLS_TYPE) FOR SKILLS_TYPE IN ([M], [A], [O], [NULL]) ) AS PIVOTTABLE
	)	--CTE_PIVOT

--SELECT * FROM CTE_PIVOT 

, CTE_BS AS
	(	SELECT B.*, P.MFG_COUNT, P.FIN_COUNT, P.O_COUNT, P.NULL_COUNT, 
			CASE
				WHEN B.BUS_STREAM = 'CMP-MFG' AND FIN_COUNT > 2 THEN 'CMP-MFGFIN'
				WHEN B.BUS_STREAM = 'CMP-FIN' AND MFG_COUNT > 2 THEN 'CMP-FINMFG'
				WHEN B.BUS_STREAM = 'MFG' AND FIN_COUNT > 2 THEN 'MFGFIN'
				WHEN B.BUS_STREAM = 'FIN' AND MFG_COUNT > 2 THEN 'FINMFG'
				ELSE B.BUS_STREAM
			END AS UPD_BUS_STREAM
		FROM CTE_BASE B WITH (NOLOCK)
		LEFT OUTER JOIN CTE_PIVOT P WITH (NOLOCK) ON B.BASE_ID = P.BASE_ID AND 
			B.LOT_ID = P.LOT_ID AND B.SPLIT_ID = P.SPLIT_ID AND B.SUB_ID = P.SUB_ID
	)	--CTE_BS

--SELECT * FROM CTE_BS

, CTE_REMAINING AS
	(	SELECT * FROM CTE_BS WITH (NOLOCK)
		where CO_STATUS = 'R' AND SHIP_SHORT <> 'C'
			AND ORDER_QTY > TOTAL_SHIPPED_QTY
			AND TOTAL_SHIPPED_QTY > 0
	)

, CTE_FULL AS 
	(	SELECT 'BASE' AS CTE, *
		FROM CTE_BS
		UNION ALL
		SELECT 'REMAINING' AS CTE , CUSTOMER_ID
			, CUST_ORDER_ID , COL_LINE_NO , ORDER_DATE, CREATE_DATE
			, DESIRED_SHIP_DATE, [FILTER_CASE], DESIRED_SHIP_DATE AS FILTER_DATE
			, CO_STATUS, [UPDATE_USING_MISC_REF], SERVICE_CHARGE_ID, LINE_TYPE
			, LINE_ITEM, CHARGE_TYPE, CUST_PART_DESC, COL_PARTID, P2ID, PART_ID
			, [DESCRIPTION], PART_PROD_CODE, PART_COMM_CODE, PLANNER_USER_ID
			, BUYER_USER_ID, REASONING, SHIP_SHORT, ORDER_QTY, TOTAL_SHIPPED_QTY
			, UNSHIPPEDQTY, REMQTY, UNIT_PRICE, BUS_STREAM, MATERIAL_TYPE
			, NULL AS 'PACKLIST_ID', NULL AS 'PACKLIST_LINE_NO'
			, NULL AS 'SHIPPED_QTY', NULL AS 'SHIPPED_DATE'
			, NULL AS 'INVOICE_DATE', NULL AS 'INVOICE_ID'
			, NULL AS 'INVOICE_LINE', NULL AS 'INVOICE_AMT'
			, NULL AS 'INVOICE_QTY'
			, SHIPPED_AMOUNT, UNSHIPPED_AMOUNT, ADJUSTED_DUE_DATE
			, SUPPLY_TYPE, BASE_ID, LOT_ID, SPLIT_ID, SUB_ID, ORDER_STATUS
			, MFG_COUNT, FIN_COUNT, O_COUNT, NULL_COUNT, UPD_BUS_STREAM
		FROM CTE_REMAINING
	)	--CTE_FULL

--, CTE_FINAL AS

	SELECT A.* 
		, MAX(DATEADD(DD,-(DAY(FILTER_DATE)-1), FILTER_DATE)) AS 'FILTER_PERIOD'
		, PSF.[Target] AS 'TARGET_GOALS'
	INTO #CTE_FINAL
	FROM CTE_FULL A
	LEFT OUTER JOIN [SQL-Bi-1].[LIVESupplemental].[dbo].[Plant_Shipment_Forecast] PSF WITH (NOLOCK)
		ON PSF.BusStream = A.BUS_STREAM AND PSF.[Date] = DATEADD(DD,-(DAY(A.FILTER_DATE)-1), A.FILTER_DATE)
	GROUP BY CTE, CUSTOMER_ID, CUST_ORDER_ID, COL_LINE_NO
	, ORDER_DATE, CREATE_DATE, DESIRED_SHIP_DATE
	, [FILTER_CASE], FILTER_DATE, CO_STATUS
	, [UPDATE_USING_MISC_REF], SERVICE_CHARGE_ID
	, LINE_TYPE, LINE_ITEM, CHARGE_TYPE
	, CUST_PART_DESC, COL_PARTID, P2ID
	, PART_ID, [DESCRIPTION], PART_PROD_CODE
	, PART_COMM_CODE, PLANNER_USER_ID, BUYER_USER_ID
	, REASONING, SHIP_SHORT, ORDER_QTY
	, TOTAL_SHIPPED_QTY, UNSHIPPEDQTY, REMQTY
	, UNIT_PRICE, BUS_STREAM, MATERIAL_TYPE
	, PACKLIST_ID, PACKLIST_LINE_NO, SHIPPED_QTY
	, SHIPPED_DATE, INVOICE_DATE, INVOICE_ID
	, INVOICE_LINE, INVOICE_AMT, INVOICE_QTY
	, SHIPPED_AMOUNT, UNSHIPPED_AMOUNT
	, ADJUSTED_DUE_DATE, SUPPLY_TYPE, BASE_ID
	, LOT_ID, SPLIT_ID, SUB_ID, ORDER_STATUS
	, MFG_COUNT, FIN_COUNT, O_COUNT, NULL_COUNT
	, UPD_BUS_STREAM, PSF.[Target]

