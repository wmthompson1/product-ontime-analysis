	, CASE
		WHEN REPLACE(REPLACE(SPECTAB_VERBIAGE, CHAR(13), ''), CHAR(10), '') = VENDOR_QCNOTES THEN NULL
		ELSE SPECTAB_VERBIAGE
	END AS SPECTAB_VERBIAGE
--> PART SPECIFICATIONS: KEEP PART OVER PO AND POL
	, PAB_LONG_DESCRIPTION
--> PO SPECIFICATIONS: KEEP VENDOR OVER POL
	, CASE
		WHEN REPLACE(REPLACE(PO_LONG_DESCRIPTION, CHAR(13), ''), CHAR(10), '') = REPLACE(REPLACE(PAB_LONG_DESCRIPTION, CHAR(13), ''), CHAR(10), '') THEN NULL
		WHEN REPLACE(REPLACE(PO_LONG_DESCRIPTION, CHAR(13), ''), CHAR(10), '') = REPLACE(REPLACE(SPECTAB_VERBIAGE, CHAR(13), ''), CHAR(10), '') THEN NULL
		ELSE PO_LONG_DESCRIPTION
	END AS PO_LONG_DESCRIPTION
--> POL SPECIFICATIONS: KEEP PO OVER POL
	, POL_LONG_DESCRIPTION AS B4_POL
	, CASE
		WHEN REPLACE(REPLACE(POL_LONG_DESCRIPTION, CHAR(13), ''), CHAR(10), '') = REPLACE(REPLACE(PO_LONG_DESCRIPTION, CHAR(13), ''), CHAR(10), '') THEN NULL
		WHEN REPLACE(REPLACE(POL_LONG_DESCRIPTION, CHAR(13), ''), CHAR(10), '') = REPLACE(REPLACE(PAB_LONG_DESCRIPTION, CHAR(13), ''), CHAR(10), '') THEN NULL
		ELSE POL_LONG_DESCRIPTION
