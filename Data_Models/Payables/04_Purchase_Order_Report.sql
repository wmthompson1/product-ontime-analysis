	, HasPrtLimited
	, HasVndLimited
	, IsFlaggedLimited
	, PRINTMESSAGE
	, TMXMESSAGE
	, DOCUMENT
--> tt20220120: REMOVE DUPLICATION BETWEEN SPECIFICATION LINES
	, PART_QCNOTES
--> QC NOTES; REMOVE DUPLICATION BETWEEN PART AND VENDOR QCNOTES
	, CASE	
		WHEN VENDOR_QCNOTES = PART_QCNOTES THEN NULL
		ELSE VENDOR_QCNOTES 
	END AS VENDOR_QCNOTES
