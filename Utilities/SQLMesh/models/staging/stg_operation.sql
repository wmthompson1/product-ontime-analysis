    workorder_base_id TEXT,
    workorder_lot_id TEXT,
    workorder_split_id TEXT,
    workorder_sub_id TEXT,
    sequence_no INT,
    resource_id TEXT,
    setup_hrs DOUBLE,
    run DOUBLE,
    run_type TEXT,
    load_size_qty DOUBLE,
    run_hrs DOUBLE,
    move_hrs DOUBLE,
    transit_days DOUBLE,
    service_id TEXT,
    scrap_yield_pct DOUBLE,
    scrap_yield_type TEXT,
    fixed_scrap_units DOUBLE,
    minimum_move_qty DOUBLE,
    calc_start_qty DOUBLE,
    calc_end_qty DOUBLE,
    completed_qty DOUBLE,
    deviated_qty DOUBLE,
    act_setup_hrs DOUBLE,
    act_run_hrs DOUBLE,
    status TEXT,
    setup_completed TEXT,
    service_begin_date TIMESTAMP,
    close_date TIMESTAMP,
    operation_type TEXT,
    drawing_id TEXT,
    drawing_rev_no TEXT,
    sched_start_date TIMESTAMP,
    sched_finish_date TIMESTAMP,
    could_finish_date TIMESTAMP,
    setup_cost_per_hr DOUBLE,
    run_cost_per_hr DOUBLE,
    run_cost_per_unit DOUBLE,
