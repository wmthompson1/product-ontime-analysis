    act_ttl_bur_cost DOUBLE,
    act_ttl_ser_cost DOUBLE,
    percent_compl TEXT,
    dispatched_qty DOUBLE,
    vendor_id TEXT,
    site_id TEXT
  ),
  grain (
    workorder_type,
    workorder_base_id,
    workorder_lot_id,
    workorder_split_id,
    workorder_sub_id,
    sequence_no
  ),
  audits (
    NOT_NULL(columns = (workorder_base_id, sequence_no))
