  EST_TTL_LAB_COST::DOUBLE AS est_ttl_lab_cost,
  EST_TTL_BUR_COST::DOUBLE AS est_ttl_bur_cost,
  EST_TTL_SER_COST::DOUBLE AS est_ttl_ser_cost,
